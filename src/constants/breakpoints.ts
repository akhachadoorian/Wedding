/** Max width (px) for mobile layouts. Below this value is considered mobile. */
export const BREAKPOINT_MOBILE = 450;

/** Max width (px) for tablet layouts. Between BREAKPOINT_MOBILE and this value is tablet. */
export const BREAKPOINT_TABLET = 768;

/** Max width (px) for desktop layouts. Above this value is considered desktop. */
export const BREAKPOINT_DESKTOP = 1024;